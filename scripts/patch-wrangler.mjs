import {
  readFileSync, writeFileSync, cpSync, rmSync,
  existsSync, readdirSync,
} from 'node:fs';
import { fileURLToPath } from 'node:url';
import { join } from 'node:path';

const root = fileURLToPath(new URL('..', import.meta.url));
const dist = join(root, 'dist');
const clientDir = join(dist, 'client');

// Flatten dist/client/ → dist/ so static files resolve at the correct URL paths.
// CF Pages serves files from the output dir root, so /_astro/main.js must live
// at dist/_astro/main.js, not dist/client/_astro/main.js.
for (const entry of readdirSync(clientDir)) {
  cpSync(join(clientDir, entry), join(dist, entry), { recursive: true });
}
rmSync(clientDir, { recursive: true });

// CF Pages Advanced Mode: a _worker.js in the output directory is picked up
// automatically as the SSR Worker without needing a wrangler.json redirect.
writeFileSync(
  join(dist, '_worker.js'),
  'export { default } from "./server/entry.mjs";\n',
);

// Keep dist/server/ out of the CDN asset upload.
// The adapter moved .assetsignore here (from dist/client/) during the flatten step.
const assetsIgnorePath = join(dist, '.assetsignore');
const existing = existsSync(assetsIgnorePath)
  ? readFileSync(assetsIgnorePath, 'utf8').trimEnd()
  : '';
if (!existing.split('\n').includes('server/')) {
  writeFileSync(assetsIgnorePath, existing + '\nserver/\n');
}

// Delete the wrangler redirect config generated by @cloudflare/vite-plugin.
// Without it CF Pages falls through to _worker.js discovery instead of trying
// to validate dist/server/wrangler.json (which has an unsolvable conflict between
// the "main" and "pages_build_output_dir" keys).
const deployConfig = join(root, '.wrangler', 'deploy', 'config.json');
if (existsSync(deployConfig)) rmSync(deployConfig);

console.log('patched: flattened assets, created _worker.js, cleared wrangler redirect');
